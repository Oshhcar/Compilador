package analizador.ast.entorno;

/**
 *
 * @author oscar
 */
public class Simbolo {
    private Tipo tipo;
    private Rol rol;
    private String id;
    private int tamaño;
    private Object valor;

    public Simbolo(Tipo tipo, Rol rol, String id, int tamaño) {
        this.tipo = tipo;
        this.rol = rol;
        this.id = id;
        this.tamaño = tamaño;
        this.valor = null;
    }

    public Simbolo(Tipo tipo, Rol rol, String id, int tamaño, Object valor) {
        this.tipo = tipo;
        this.rol = rol;
        this.id = id;
        this.tamaño = tamaño;
        this.valor = valor;
    }

    /**
     * @return the tipo
     */
    public Tipo getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(Tipo tipo) {
        this.tipo = tipo;
    }

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the valor
     */
    public Object getValor() {
        return valor;
    }

    /**
     * @param valor the valor to set
     */
    public void setValor(Object valor) {
        this.valor = valor;
    }

    /**
     * @return the rol
     */
    public Rol getRol() {
        return rol;
    }

    /**
     * @param rol the rol to set
     */
    public void setRol(Rol rol) {
        this.rol = rol;
    }

    /**
     * @return the tamaño
     */
    public int getTamaño() {
        return tamaño;
    }

    /**
     * @param tamaño the tamaño to set
     */
    public void setTamaño(int tamaño) {
        this.tamaño = tamaño;
    }
    
    
    
}
